    /**
     * 开发用 获得scale
     * @param time
     * @return
     */

    // The gesture threshold expressed in dp （定义的16dp的值）
    private static final float GESTURE_THRESHOLD_DP = 16.0f;
    // Get the screen's density scalefinal （获取缩放比例）
    public int getScreenScale(Context context){
        float scale = context.getResources().getDisplayMetrics().density;
// Convert the dps to pixels, based on density scale （定义的值 *　比例 ＋ 0.5f）
        int mGestureThreshold = (int) (GESTURE_THRESHOLD_DP * scale + 0.5f);
        return mGestureThreshold;
    }


    public static String getFormatTime(long time){

        SimpleDateFormat format=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        Date d1=new Date(time);
        String t1=format.format(d1);
        return t1;
    }



    public static Bitmap drawableToBitmap(Drawable drawable) {



        Bitmap bitmap = Bitmap.createBitmap(

                drawable.getIntrinsicWidth(),

                drawable.getIntrinsicHeight(),

                drawable.getOpacity() != PixelFormat.OPAQUE ? Bitmap.Config.ARGB_8888

                        : Bitmap.Config.RGB_565);

        Canvas canvas = new Canvas(bitmap);

        //canvas.setBitmap(bitmap);

        drawable.setBounds(0, 0, drawable.getIntrinsicWidth(), drawable.getIntrinsicHeight());

        drawable.draw(canvas);

        return bitmap;

    }


